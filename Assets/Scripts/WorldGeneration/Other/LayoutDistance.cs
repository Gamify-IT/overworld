using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class LayoutDistance
{
    public int left;
    public int up;
    public int right;
    public int down;

    public LayoutDistance(int left, int up, int right, int down)
    {
        this.left = left;
        this.up = up;
        this.right = right;
        this.down = down;
    }
}
